        // Animation on scroll
        function initAnimations() {
            const observerOptions = {
                threshold: 0.1,
                rootMargin: '0px 0px -50px 0px'
            };

            const observer = new IntersectionObserver(function(entries) {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.style.opacity = '1';
                        entry.target.style.transform = 'translateY(0)';
                    }
                });
            }, observerOptions);

            // Observe elements for animation
            const animatedElements = document.querySelectorAll('.service-card, .stat-card, .mission-card, .team-card, .reason-card, .benefit-item');

            animatedElements.forEach(el => {
                el.style.opacity = '0';
                el.style.transform = 'translateY(30px)';
                el.style.transition = 'opacity 0.6s ease-out, transform 0.6s ease-out';
                observer.observe(el);
            });
        }

        // Contact form functionality
        function initContactForm() {
            const contactForm = document.getElementById('contactForm');

            if (contactForm) {
                contactForm.addEventListener('submit', function(e) {
                    e.preventDefault();

                    // Get form data
                    const formData = new FormData(this);
                    const data = {};

                    for (let [key, value] of formData.entries()) {
                        data[key] = value;
                    }

                    // Basic validation
                    if (!data.firstName || !data.lastName || !data.email || !data.message) {
                        showNotification('Please fill in all required fields.', 'error');
                        return;
                    }

                    if (!isValidEmail(data.email)) {
                        showNotification('Please enter a valid email address.', 'error');
                        return;
                    }

                    // Simulate form submission
                    const submitButton = this.querySelector('button[type="submit"]');
                    const originalText = submitButton.textContent;

                    submitButton.textContent = 'Sending...';
                    submitButton.disabled = true;

                    // Simulate API call
                    setTimeout(() => {
                        showNotification('Thank you! Your message has been sent successfully.', 'success');
                        this.reset();

                        submitButton.textContent = originalText;
                        submitButton.disabled = false;
                    }, 2000);
                });
            }
        }

        // Utility functions
        function isValidEmail(email) {
            const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
            return emailRegex.test(email);
        }

        function showNotification(message, type = 'info') {
            // Create notification element
            const notification = document.createElement('div');
            notification.className = `notification notification-${type}`;
            notification.textContent = message;

            // Add styles
            notification.style.cssText = `
        position: fixed;
        top: 20px;
        right: 20px;
        padding: 16px 24px;
        border-radius: 8px;
        color: white;
        font-weight: 600;
        z-index: 9999;
        opacity: 0;
        transform: translateX(100%);
        transition: all 0.3s ease;
    `;

            // Set background color based on type
            if (type === 'success') {
                notification.style.background = 'linear-gradient(135deg, #059669, #047857)';
            } else if (type === 'error') {
                notification.style.background = 'linear-gradient(135deg, #dc2626, #b91c1c)';
            } else {
                notification.style.background = 'linear-gradient(135deg, #2563eb, #1d4ed8)';
            }

            // Add to DOM
            document.body.appendChild(notification);

            // Animate in
            setTimeout(() => {
                notification.style.opacity = '1';
                notification.style.transform = 'translateX(0)';
            }, 100);

            // Remove after 5 seconds
            setTimeout(() => {
                notification.style.opacity = '0';
                notification.style.transform = 'translateX(100%)';

                setTimeout(() => {
                    if (notification.parentNode) {
                        notification.parentNode.removeChild(notification);
                    }
                }, 300);
            }, 5000);
        }

